#include <iostream>


int main() {
    // 在标准输出中打印 "Hello, world!"
    std::cout << "Hello, world!" << std::endl;
    int a = 1;
    int b = 2;
    int c = 3;
    int d = 4;
    int e = 5;
    std::cout << "a = " << a << std::endl;
    std::cout << "b = " << b << std::endl;
    std::cout << "c = " << c << std::endl;
    std::cout << "d = " << d << std::endl;
    std::cout << "e = " << e << std::endl;
    return 0;
}

// 此文件编译运行将输出 "Hello, world!"。


